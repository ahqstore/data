const { existsSync } = require("node:fs");
const path = require("node:path");

const rand = () => {
  let id = "";

  for (let i = 0; i < 128; i++) {
    id += symbols[Math.floor(Math.random() * symbols.length)];
  }

  if (existsSync(path.join(__dirname, "..", "..", "developers", id))) {
    return rand();
  }

  return id;
};

